            <div class="fondatoreStoria">Phineas Taylor Barnum nacque a Bethel, nel Connecticut, 
                da Phillo Barnum (1778-1826), locandiere, sarto e magazziniere, e dalla sua seconda moglie Irene Taylor, 
                che ebbe complessivamente dieci figli.
                <br>
                Suo nonno materno Phineas Taylor era un famoso legislatore, 
                proprietario terriero e giudice di pace. Barnum, che era molto portato per la matematica, 
                ma detestava i lavori manuali, iniziò a lavorare come magazziniere. Barnum divenne famoso per aver creato 
                l'American Museum nel 1842, che fu distrutto due volte da un incendio, e il circo chiamato The Greatest Show on Earth 
                (Il più grande spettacolo del mondo) nel 1872, un enorme circo a tre piste e con ben quattro palcoscenici, 
                che poteva ospitare ventimila spettatori, anch'esso distrutto in un incendio. Fu sindaco di Bridgeport dal 1875 al 1876.
                Nel 1880 si unì al suo più temibile rivale, James Anthony Bailey, formando un'enorme struttura dove trovarono lavoro oltre 
                mille persone, artisti compresi, oltre a trenta elefanti e un grande numero di cavalli, leoni, orsi, e così via.
                Dopo la morte di Barnum, Bailey continuò l'ormai avviato successo del circo, facendolo giungere sino ai giorni nostri. 
